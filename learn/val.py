# 标准数据类型
# Numbers，String，List，Tuple，Dictionary


# String
label = "abcde"

print(label[1:2])
print(label[1:])
print(label[:4])
print(label * 2)

# List
arr = ["a", "b", "c", "d", "e"]

print(arr[1:2])
print(arr[1:])
print(arr[:4])
print(arr * 2)

# Tuple 因为元组是不允许更新的。而列表是允许更新的：
tuple_test = ('runoob', 786, 2.23, 'john', 70.2)


print(tuple_test[1:2])
print(tuple_test[1:])
print(tuple_test[:4])
print(tuple_test * 2)


